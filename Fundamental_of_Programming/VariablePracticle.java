package Fundamental_of_Programming;

public class VariablePracticle 
{

	public static void main(String[] args) 
	{
		int age=10;
		System.out.println("Before the age of a person : "+age);
		
		age = 20;
		System.out.println("After the age of a person is change : "+age);
		System.out.println("Conclusion here is that the data type hold the value one time at one");
		System.out.println("*******************************************");
		
		String name = "Raju Kushwah";
		System.out.println("Before Name of person : "+name);
		
		name = "Arju Kapoor";
		System.out.println("After Name of person : "+name);
		
		char capitalLetter = 'A';
		System.out.println("Before here is capital letter is print : "+capitalLetter);
		capitalLetter = 'B';
		System.out.println("After here is capital letter is print : "+capitalLetter);
		
		
	}

}
