package Fundamental_of_Programming;

public class OperaorCode {

	public static void main(String[] args) {
		//declare variables p and q
		int p=20,q=10;
		int result;
		
		//addition operator
		result = p+q;
		System.out.println(result);
		
		//subtraction operator
		//we can directly perform subtraction in print statement, no need to use result vriable here
		
		System.out.println(p-q);
		
		//division operator
		System.out.println(p/q);
		
		//multiplication operator
		System.out.println(p*q);
		
		//modulo operator
		System.out.println(p%q);

	}

}
